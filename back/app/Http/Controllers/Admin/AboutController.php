            'description' => 'required',
        ]);

        $data = $request->only(['description']);

        if ($request->hasFile('image')) {
            $path = $request->file('image')->store('about', 'public');
            $data['image'] = $path;
        }

     About::create($data);

        return redirect()->back()->with('success', 'About content added successfully');
